import numpy as np
import torch


class BaseNoise:
    def __call__(self, data):
        return self.forward(data)

    def forward(self, data):
        pass


class GaussianNoise(BaseNoise):
    def __init__(self, sigma):
        self.sigma = sigma

    def forward(self, data):
        return data + torch.randn_like(data, device=data.device) * self.sigma


